                    <div class="lead">
                    <p>Residents have basic, universal rights that must be protected. This includes necessities such as the right to be treated with dignity and respect etc. As Sheltered Housing Ltd, we will ensure that the supported accommodations we partner with will meet the utmost standards and prioritize protecting the rights and freedoms of the residents.</p>
                    </div>
