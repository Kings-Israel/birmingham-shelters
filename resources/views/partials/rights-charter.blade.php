        </div>

        <div class="row">
            <div class="col-lg-6 col-md-12 text-center">
                <div class="sec-heading center">
                    <h4>1. A right to freedom from discrimination</h4>
                    <div class="lead">
                    <p>The homeless people have a right to not be subjected to discrimination based on race, sex, age, or creed etc. The services offered to the residents should be consistent, fair, and based on strong anti-discrimination standards.</p>
                    </div>
                </div>
            </div>

            <div class="col-lg-6 col-md-12 text-center">
                <div class="sec-heading center">
                    <div class="lead">
                    <h4>2. A right to feel secure and safe.</h4>
                    <p>Living settings should be secure, safe, and well-managed to promote excellent health and happiness. Residents must feel safe to live happily and progress. Providers must strictly adhere to their protection and risk management responsibilities. The residents should be granted freedom from harassment, neglect, fear, and abuse.</p>
                    </div>
                </div>
            </div>
        </div>

        <div class="row">
            <div class="col-lg-6 col-md-12 text-center">
                <div class="sec-heading center">
                    <h4>3. A right to accurate information about your support eligibility.</h4>
                    <div class="lead">
                    <p>Residents should have open lines of communication with their housing providers, be engaged in decisions that affect them, and be consulted on a regular basis to ensure that their housing or support is meeting their needs. This includes clear and effective complaint management systems. </p>
                    </div>
                </div>
            </div>

            <div class="col-lg-6 col-md-12 text-center">
                <div class="sec-heading center">
                    <h4>4. A right to privacy and confidentiality</h4>
                    <div class="lead">
                    <p>Residents' right to privacy and confidentiality must be respected and always preserved, particularly during one-on-one or support sessions.</p>
                    </div>
                </div>
            </div>
        </div>

        <div class="row justify-content-center">
            <div class="col-lg-7 col-md-10 text-center">
                <div class="sec-heading center">
                    <h4>5. A right to an adequate living standard</h4>
                    <div class="lead">
                    <p>Everyone has the right to an appropriate quality of living for oneself and his or her family, including appropriate food, clothing, and housing, as well as the right to see their living conditions improve over time.</p>
                    </div>
                </div>
            </div>
        </div>

